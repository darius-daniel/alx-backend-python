#!/usr/bin/env python3
"""
Contains a function that takes two strings and returns a concatenated string.
"""


def concat(str1: str, str2: str) -> str:
    """
    Takes in two strings and returns a concatenated string

    :param str1: first string
    :param str2: second string
    :return: resultant string from the concatenation of str1 and str2
    """
    return str1 + str2
